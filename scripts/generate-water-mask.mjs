// Generates lib/waterMask.ts — a packed bitmask of water cells for the MMR.
//
// How: downloads the z12 OSM raster tiles covering the map's maxBounds once,
// classifies pixels by the OSM standard water color (#aad3df), and downsamples
// 3×3 pixels (~110m) into one cell. A cell counts as water only if ≥8 of its
// 9 pixels are water, so shoreline cells stay pickable and only solid water
// (sea, creeks, lakes) is blocked.
//
// Run: node scripts/generate-water-mask.mjs   (re-run only if bounds change)

import { writeFileSync, mkdirSync, existsSync, readFileSync } from "fs";
import { fileURLToPath } from "url";
import { PNG } from "pngjs";

const ZOOM = 12;
const SCALE = 3; // downsample factor, px per cell edge

// Must cover MMR_BOUNDS in components/MapView.tsx: [72.6,18.75]..[73.35,19.5]
const WEST = 72.6, SOUTH = 18.75, EAST = 73.35, NORTH = 19.5;

const n = 2 ** ZOOM;
const lngToX = (lng) => ((lng + 180) / 360) * n;
const latToY = (lat) =>
  ((1 - Math.asinh(Math.tan((lat * Math.PI) / 180)) / Math.PI) / 2) * n;

const x0 = Math.floor(lngToX(WEST));
const x1 = Math.floor(lngToX(EAST));
const y0 = Math.floor(latToY(NORTH)); // y grows southward in tile coords
const y1 = Math.floor(latToY(SOUTH));

const tilesX = x1 - x0 + 1;
const tilesY = y1 - y0 + 1;
const canvasW = tilesX * 256;
const canvasH = tilesY * 256;
console.log(`tiles: ${tilesX}×${tilesY} (${tilesX * tilesY}), canvas ${canvasW}×${canvasH}px`);

const cacheDir = fileURLToPath(new URL("./.tile-cache/", import.meta.url));
mkdirSync(cacheDir, { recursive: true });

async function fetchTile(x, y) {
  const cached = `${cacheDir}${ZOOM}-${x}-${y}.png`;
  if (existsSync(cached)) return readFileSync(cached);
  const res = await fetch(`https://tile.openstreetmap.org/${ZOOM}/${x}/${y}.png`, {
    headers: { "User-Agent": "BombayRent-water-mask-generator/1.0 (one-time build script)" },
  });
  if (!res.ok) throw new Error(`tile ${x},${y}: HTTP ${res.status}`);
  const buf = Buffer.from(await res.arrayBuffer());
  writeFileSync(cached, buf);
  await new Promise((r) => setTimeout(r, 150)); // be polite to the tile server
  return buf;
}

// water[py * canvasW + px] = 1 when that pixel is OSM water blue
const water = new Uint8Array(canvasW * canvasH);
const isWaterRGB = (r, g, b) =>
  Math.abs(r - 170) <= 10 && Math.abs(g - 211) <= 10 && Math.abs(b - 223) <= 10;

for (let ty = 0; ty < tilesY; ty++) {
  for (let tx = 0; tx < tilesX; tx++) {
    const png = PNG.sync.read(await fetchTile(x0 + tx, y0 + ty));
    for (let py = 0; py < 256; py++) {
      for (let px = 0; px < 256; px++) {
        const i = (py * 256 + px) * 4;
        if (isWaterRGB(png.data[i], png.data[i + 1], png.data[i + 2])) {
          water[(ty * 256 + py) * canvasW + (tx * 256 + px)] = 1;
        }
      }
    }
    process.stdout.write(".");
  }
}
console.log(" decoded");

const width = Math.floor(canvasW / SCALE);
const height = Math.floor(canvasH / SCALE);
const bits = new Uint8Array(Math.ceil((width * height) / 8));
let waterCells = 0;

for (let row = 0; row < height; row++) {
  for (let col = 0; col < width; col++) {
    let count = 0;
    for (let dy = 0; dy < SCALE; dy++)
      for (let dx = 0; dx < SCALE; dx++)
        count += water[(row * SCALE + dy) * canvasW + (col * SCALE + dx)];
    if (count >= 8) {
      const idx = row * width + col;
      bits[idx >> 3] |= 1 << (idx & 7);
      waterCells++;
    }
  }
}
console.log(
  `grid ${width}×${height}, water cells: ${waterCells} (${((waterCells / (width * height)) * 100).toFixed(1)}%)`
);

const b64 = Buffer.from(bits).toString("base64");
const out = `// GENERATED by scripts/generate-water-mask.mjs — do not edit by hand.
// Water bitmask for the Mumbai Metropolitan Region, derived from OSM z${ZOOM}
// tiles. One bit per ~110m cell; 1 = solid water (sea / creek / lake).

export const WATER_MASK = {
  zoom: ${ZOOM},
  x0: ${x0}, // tile-grid origin of the mask
  y0: ${y0},
  scale: ${SCALE}, // tile pixels per cell edge
  width: ${width},
  height: ${height},
  data: "${b64}",
} as const;
`;
writeFileSync(fileURLToPath(new URL("../lib/waterMask.ts", import.meta.url)), out);
console.log(`lib/waterMask.ts written (${(out.length / 1024).toFixed(0)} KB)`);
